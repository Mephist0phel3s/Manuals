// C While Loop Cheat Sheet

// 1. Basic While Loop Syntax
// The `while` loop repeats a block of code as long as the condition is true.

while (condition) {
    // Code to execute while the condition is true
}

int i = 0;
while (i < 10) {
    printf("%d\n", i);  // Prints numbers from 0 to 9
    i++;
}

// 2. Do-While Loop
// A `do-while` loop always executes at least once, since the condition is checked after the loop.

int j = 0;
do {
    printf("%d\n", j);  // Prints numbers from 0 to 9
    j++;
} while (j < 10);

// End of While Loop Cheat Sheet
